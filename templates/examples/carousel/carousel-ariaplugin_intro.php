<p>
The Carousel ARIA Plugin makes it easy to use the 
<a href="http://www.w3.org/TR/wai-aria/">WAI-ARIA Roles and States</a> with the Carousel control.
Using the ARIA plugin, a Carousel is more interoperable with assistive technologies (AT),
such as screen readers, making it more accessible to users with disabilities.
</p>
<p>
<a href="http://video.yahoo.com/watch/3609568/9957126">Watch a screen cast of this example 
running in Firefox 3 with the NVDA screen reader</a>, to see immediately the benefits that 
ARIA provides, or 
<a href="http://www.nvda-project.org/wiki/Snapshots">download the latest development snapshot of 
NVDA</a> to test this example for yourself.
</p>