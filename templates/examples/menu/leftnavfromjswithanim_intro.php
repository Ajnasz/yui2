<p>
This example demonstrates how to create a traditional, two-column page layout
(<a href="../../grids/">using Grids</a>) with an set of animated fly-out menu 
navigation in the left column.  The left navigation is created using a Menu 
instance that is constructed using existing markup and enables the user to 
navigate to different landing pages for each Yahoo! product category.  If 
JavaScript is enabled, submenus are appended to the items in the Menu, allowing
the user to skip the product landing pages and proceed directly to a 
given property.
</p>