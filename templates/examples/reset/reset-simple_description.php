<h2 class="first">About This Example</h2>

<p>The normalization provided by <a href="http://developer.yahoo.com/yui/reset/">YUI Reset CSS</a> includes the following characteristics:</p>

<ul>
	<li>black text on a white page background,</li>
	<li>margin and padding to zero,</li>
	<li>table borders to zero,</li>
	<li>no visual list style (<code>ol</code>, <code>ul</code>, <code>dl</code>),</li>
	<li>all fonts at 100% of base,</li>
	<li>font-weight and font-style to normal,</li>
	<li>better (but still incomplete) font inheritance into forms,</li>
	<li>reduced line-height disruption from <code>sub</code> and <code>sup</code>.</li>
</ul>

<p><a href="reset-simple_source.html" target="_blank">Open this example is a new window</a> to see a test page of HTML elements with YUI Reset applied.</p>
