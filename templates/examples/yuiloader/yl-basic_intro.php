<p>In this example, we bring a YUI component onto the page using the <a href="http://developer.yahoo.com/yui/loader/">YUI Loader Utility</a>. This example implements YAHOO.util.YUILoader.</p>

<p>This example loads in new window to demonstrate the simplest use case &mdash; a page with no other content, wherein the Loader is introducing YUI into a simple context.</p>
