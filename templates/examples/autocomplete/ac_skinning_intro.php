<p>This example demonstrates the CSS applied to an implementation of the AutoComplete
control that uses the "Sam Skin". You can read more about the general approach
to skinning YUI components
<a href="http://developer.yahoo.com/yui/articles/skinning/">in this in-depth article</a>. Change any or all
of the skin and implementation CSS for a more customized look and feel.</p>

<p>In order to enable the new skinning model, AutoComplete expects a wrapper DIV element
around the INPUT element and the container DIV element, in this fashion:</p>

<pre>
    &lt;div id="myAutoComplete"&gt;
        &lt;input type="text" id="myInput"&gt;
        &lt;div id="myContainer"&gt;&lt;/div&gt;
    &lt;/div&gt;
</pre>
